// Java0207.java
// This program displays several number words.
// The focus now is on program comments.
// Program comments aid in "program documentation" and make your program more readable.
// Every line that begins with a "double slash" is considered a "comment" by the Java compiler.
// The Java compiler ignores all comments.  They are not compiled.  They are not executed.
// If a line begins with a double slash, it is simply ignored by the compiler.
// That is precisely what is happening with the first 12 lines of this program.
// Note below that a comment can also be placed in the middle of the program.
// They can even be placed right after a program statement on the same line.
// Note: The word "Thirteen" is not displayed because it has been "commented-out"
// possibly by someone suffering from Triskaidekaphobia (the fear of the number 13).


public class Java0207
{
	public static void main (String args[])
	{
		System.out.println("One");
		System.out.println("Two");
		System.out.println("Three");
		System.out.println("Four");
		System.out.println("Five");
		System.out.println("Six");        // half a dozen
		System.out.println("Seven");
		System.out.println("Eight");
		System.out.println("Nine");
		System.out.println("Ten");
		System.out.println("Eleven");
		System.out.println("Twelve");     // one dozen
//		System.out.println("Thirteen");   // one baker's dozen
	}
}


